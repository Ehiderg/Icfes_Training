export function getQuestionsByArea(area) {
  return QUESTIONS.filter(q => q.area === area)
}

export function getAllAreas() {
  return Object.values(AREAS)
}

export function shuffleArray(array) {
  const arr = [...array]
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]]
  }
  return arr
}

export function getQuickTestQuestions(areas = null, count = 10) {
  const pool = areas
    ? QUESTIONS.filter(q => areas.includes(q.area))
    : QUESTIONS
  return shuffleArray(pool).slice(0, count)
}

// Returns a shuffled simulacro with exactly the official ICFES question count per area
export function getFullTestQuestions() {
  const result = []
  Object.entries(FULL_TEST_COUNTS).forEach(([area, count]) => {
    const pool = shuffleArray(QUESTIONS.filter(q => q.area === area))
    result.push(...pool.slice(0, Math.min(count, pool.length)))
  })
  return shuffleArray(result)
}

